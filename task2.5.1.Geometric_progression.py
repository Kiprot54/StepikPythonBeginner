print('Геометрическая прогрессия')
print()
b1 = int(input('Введи первый член геометрической прогрессии: '))
q = int(input('Введи знаменатель геометрической прогрессии: '))
while True:
    n = input('Какой член геометрической прогрессии нужно найти? ')
    if not n.isdigit():
        print('Нужно ввести целое положительное число')
        print()
    else:
        print(f'{n}-й член геометрической прогрессии:', b1 * q ** (int(n) - 1))
        break
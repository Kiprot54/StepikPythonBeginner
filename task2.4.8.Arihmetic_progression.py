print('Арифметическая прогрессия')
print()
a1 = int(input('Введи первый член арифметической прогрессии: '))
d = int(input('Введи разность арифметической прогрессии: '))
while True:
    n = input('Какой член арифметической прогрессии нужно найти? ')
    if not n.isdigit():
        print('Нужно ввести целое положительное число')
        print()
    else:
        print(f'{n}-й член арифметической прогрессии:', a1 + d * (int(n) - 1))
        break